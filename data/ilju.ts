// 주어진 일주에 대해 잘 맞는/조심할 일주 3개씩 반환
export function getIljuCompat(ilju: string): { good: string[]; conflict: string[] } {
  const branch = ilju[1];
  const harmonyBranch = BRANCH_HARMONY[branch];
  const conflictBranch = BRANCH_CONFLICT[branch];

  const all = Object.keys(ILJU);
  const good = all.filter((i) => i[1] === harmonyBranch).slice(0, 3);
  const conflict = all.filter((i) => i[1] === conflictBranch).slice(0, 3);
  return { good, conflict };
}
